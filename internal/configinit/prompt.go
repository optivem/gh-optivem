// langChoices is the shared option list for monolith/backend lang
// prompts. Must stay in sync with config.ValidateBackendLang — the
// accept callback still runs that validator so a drift here surfaces
// rather than landing in the YAML.
var langChoices = []string{"java", "dotnet", "typescript"}

// licenseChoices enumerates the SPDX-like keys IsValidLicense accepts.
// Order matters for the numbered prompt; defaultLicense is highlighted
// as the ENTER default.
var licenseChoices = []string{"mit", "apache-2.0", "gpl-3.0", "bsd-2-clause", "bsd-3-clause", "unlicense"}

// askLicense collects the license key for the scaffolded LICENSE file.
// ENTER selects defaultLicense, parallel to the flag default in
// bindYAMLAffectingFlags.
func askLicense(r *bufio.Reader, out io.Writer, f *config.RawFlags) error {
	dflt := indexOf(licenseChoices, defaultLicense) + 1
	return askChoice(r, out, "License", licenseChoices, dflt, func(v string) error {
		if !projectconfig.IsValidLicense(v) {
			return fmt.Errorf("must be one of mit, apache-2.0, gpl-3.0, bsd-2-clause, bsd-3-clause, unlicense")
		}
		f.License = v
		return nil
	})
}

