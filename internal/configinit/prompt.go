// askProjectURL gates the GitHub Project URL behind a y/n question via
// promptio.ConfirmYN (the project-wide y/n helper, no Enter-default).
// "no" leaves f.ProjectURL empty so the flag-path semantics for an
// unset --project-url still apply. "yes" asks for the URL with full
// format + existence validation, re-asking just the URL on a bad value
// rather than discarding the whole session.
func askProjectURL(r *bufio.Reader, out io.Writer, f *config.RawFlags) error {
	has, err := approval.Confirm(pkgApproval, approval.CategoryPrompt, r, out, "  Do you have an existing GitHub Project?")
	if err != nil {
		return fmt.Errorf("read project URL gate: %w", err)
	}
	if !has {
		return nil
	}
	return ask(r, out, "GitHub Project URL, e.g. https://github.com/orgs/acme/projects/1", func(v string) (string, error) {
		if msg := config.ValidateProjectURLFormat(v); msg != "" {
			return "", fmt.Errorf("%s", msg)
		}
		if err := config.CheckProjectExists(v); err != nil {
			return "", err
		}
		f.ProjectURL = v
		return v, nil
	})
}

// indexOf returns the index of want in xs, or -1 if absent.
func indexOf(xs []string, want string) int {
	for i, x := range xs {
		if x == want {
			return i
		}
	}
	return -1
}

// applyDefaults fills the fields the prompt does NOT ask for and that
// are NOT already defaulted by config.resolvePathFlagsForYAML (tier
// paths). Today that's frontend-lang on multitier (typescript is the
// only valid value) and deploy (docker; cloud-run is in development —
// prompt for it when a real choice exists).
// TODO: prompt for frontend-lang when more than typescript is valid.
// TODO: prompt for deploy when cloud-run ships.
func applyDefaults(f *config.RawFlags) {
	f.Deploy = projectconfig.DeployDocker
	if f.Arch == "multitier" {
		f.FrontendLang = "typescript"
	}
}

// ask prints a single prompt and loops until accept returns nil. The
// caller's accept closure both validates and stores the value into the
// RawFlags. EOF (no more input — pipe closed, ^D, tests running out of
// fixture lines) is surfaced verbatim so the missing-file recovery can
// abort with the terse-error fallback rather than spin forever.
func ask(r *bufio.Reader, out io.Writer, label string, accept func(string) (string, error)) error {
	for {
		fmt.Fprintf(out, "  %s: ", label)
		line, err := r.ReadString('\n')
		if err != nil {
			return fmt.Errorf("read %s: %w", label, err)
		}
		v := strings.TrimSpace(line)
		if v == "" {
			fmt.Fprintln(out, "    value cannot be empty")
			continue
		}
		if _, err := accept(v); err != nil {
			fmt.Fprintf(out, "    %s\n", err)
			continue
		}
		return nil
	}
}

// askChoice prints a numbered list of options and reads an integer
// selection (1..N). The chosen label is echoed back ("  → java") and
// passed to accept, which still runs the same validator the flag path
// uses so a drift between the choices slice and the validator surfaces
// at runtime. defaultIdx = 0 means no default (empty input re-asks);
// defaultIdx ∈ [1, len(choices)] makes ENTER select choices[defaultIdx-1]
// and adds a "(default)" marker next to that option. Out-of-range or
// non-numeric input re-asks without consuming the rest of the script.
func askChoice(r *bufio.Reader, out io.Writer, label string, choices []string, defaultIdx int, accept func(string) error) error {
	if defaultIdx < 0 || defaultIdx > len(choices) {
		return fmt.Errorf("askChoice %q: defaultIdx %d out of range [0, %d]", label, defaultIdx, len(choices))
	}
	for {
		fmt.Fprintf(out, "  %s:\n", label)
		for i, c := range choices {
			if defaultIdx == i+1 {
				fmt.Fprintf(out, "    [%d] %s (default)\n", i+1, c)
			} else {
				fmt.Fprintf(out, "    [%d] %s\n", i+1, c)
			}
		}
		if defaultIdx > 0 {
			fmt.Fprintf(out, "  Choose [1-%d, ENTER = %d]: ", len(choices), defaultIdx)
		} else {
			fmt.Fprintf(out, "  Choose [1-%d]: ", len(choices))
		}
		line, err := r.ReadString('\n')
		if err != nil {
			return fmt.Errorf("read %s: %w", label, err)
		}
		v := strings.TrimSpace(line)
		var n int
		if v == "" {
			if defaultIdx == 0 {
				fmt.Fprintf(out, "    please enter a number between 1 and %d\n", len(choices))
				continue
			}
			n = defaultIdx
		} else {
			parsed, err := strconv.Atoi(v)
			if err != nil || parsed < 1 || parsed > len(choices) {
				fmt.Fprintf(out, "    please enter a number between 1 and %d\n", len(choices))
				continue
			}
			n = parsed
		}
		chosen := choices[n-1]
		fmt.Fprintf(out, "    → %s\n", chosen)
		if err := accept(chosen); err != nil {
			fmt.Fprintf(out, "    %s\n", err)
			continue
		}
		return nil
	}
}
